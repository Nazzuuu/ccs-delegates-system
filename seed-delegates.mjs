async function postDelegate(delegate) {
  const res = await fetch(BASE_URL, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${TOKEN}`,
    },
    body: JSON.stringify({ data: delegate }),
  })
  if (!res.ok) {
    const err = await res.text()
    throw new Error(`Failed to post "${delegate.name}": ${res.status} ${err}`)
  }
  return res.json()
}

async function seed() {
  console.log(`Starting seed — ${delegates.length} delegates to insert...\n`)
  let success = 0
  let failed = 0

  for (let i = 0; i < delegates.length; i++) {
    const d = delegates[i]
    try {
      await postDelegate(d)
      console.log(`[${i + 1}/${delegates.length}] ✓ ${d.name}`)
      success++
    } catch (e) {
      console.error(`[${i + 1}/${delegates.length}] ✗ ${e.message}`)
      failed++
    }
    // small delay to avoid overwhelming the server
    await new Promise(r => setTimeout(r, 50))
  }

  console.log(`\nDone! ✓ ${success} inserted, ✗ ${failed} failed.`)
}

